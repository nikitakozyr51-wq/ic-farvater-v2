// Парсеры аббревиатур разъёмов ЕТ-серий
// Для каждой серии: columns[] (заголовки таблицы) + parse(name) → объект значений

const ConnectorParsers = (() => {

  // ───── ЕТ-2РМТ / ЕТ-2РМДТ ─────
  // ЕТ-2РМТ 27 К ПН 10 Г 1 А 1 В
  // ЕТ-2РМДТ 27 К ПН 10 Г 1 А 1 В
  const et2rmt = {
    columns: ['Размер', 'Корпус', 'Контакты', 'Часть', 'Сочетание', 'Покрытие', 'Теплост.', 'Клим.'],
    parse(name) {
      const m = name.match(/^ЕТ-2РМ[ДD]?Т\s*(\d+)\s*([КБ])\s*(П[НЭ]|У[НЭ])?\s*(\d+)\s*([ШГ])\s*(\d+)\s*([АВ])?\s*(\d)?\s*([ВБ])?/);
      if (!m) return null;
      const housingMap = {
        'КПН': 'Кабельный прямой',
        'КПЭ': 'Кабельный прямой экран.',
        'КУН': 'Кабельный угловой',
        'КУЭ': 'Кабельный угловой экран.',
        'БПН': 'Приборный прямой',
        'БПЭ': 'Приборный прямой экран.',
        'К': 'Кабельный',
        'Б': 'Приборный'
      };
      const housing = m[2] + (m[3] || '');
      return {
        'Размер': m[1],
        'Корпус': housingMap[housing] || housing,
        'Контакты': m[4],
        'Часть': m[5] === 'Ш' ? 'Вилка' : 'Розетка',
        'Сочетание': m[6],
        'Покрытие': m[7] === 'А' ? 'Золото' : m[7] === 'В' ? 'Серебро' : '—',
        'Теплост.': m[8] === '1' ? '100°C' : m[8] === '2' ? '150°C' : '—',
        'Клим.': m[9] || 'В'
      };
    }
  };

  // ───── ЕТ-2РТТ ─────
  // ЕТ-2РТТ28КПН7Ш11ОВ  (К-тип: корпус + патрубок + экран)
  // ЕТ-2РТТ16Б1Г2В       (Б-тип: корпус, без патрубка)
  // ЕТ-2РТТ16БПН1Г2В     (Б-тип: с патрубком)
  const et2rtt = {
    columns: ['Размер', 'Корпус', 'Контакты', 'Часть', 'Сочетание', 'Кожух', 'Клим.'],
    parse(name) {
      const m = name.match(/^ЕТ-2РТТ\s*(\d+)\s*([КБ])\s*([ПУ][НЭ])?\s*(\d+)\s*([ШГ])\s*(\d+)\s*(О)?\s*(\d)?([ВБ])?/);
      if (!m) return null;
      const base = m[2] === 'К' ? 'Кабельный' : 'Приборный';
      const suffix = m[3] || '';
      return {
        'Размер': m[1],
        'Корпус': suffix ? base + ' ' + suffix : base,
        'Контакты': m[4],
        'Часть': m[5] === 'Ш' ? 'Вилка' : 'Розетка',
        'Сочетание': m[6],
        'Кожух': m[7] === 'О' ? 'Без кожуха' : 'С кожухом',
        'Клим.': m[9] || 'В'
      };
    }
  };

  // ───── ЕТ-2РМГ(Д) ─────
  // ЕТ-2РМ Г Д 22 Б ПЭ 10 Ш 1 Е 2 Б
  const et2rmg = {
    columns: ['Тип', 'Размер', 'Корпус', 'Контакты', 'Часть', 'Сочетание', 'Покрытие', 'Теплост.'],
    parse(name) {
      const m = name.match(/^ЕТ-2РМГ\s*(Д)?\s*(\d+)\s*([КБ])\s*(П[НЭ])?\s*(\d+)\s*([ШГ])\s*(\d+)\s*([ЕАВ])?\s*(\d)?\s*([БВ])?/);
      if (!m) return null;
      const coverMap = { 'Е': 'Никель', 'А': 'Золото', 'В': 'Серебро' };
      return {
        'Тип': m[1] ? 'Герм. длинные линии' : 'Герметичный',
        'Размер': m[2],
        'Корпус': (m[3] === 'Б' ? 'Приборный' : 'Кабельный') + (m[4] ? ' ' + m[4] : ''),
        'Контакты': m[5],
        'Часть': m[6] === 'Ш' ? 'Вилка' : 'Розетка',
        'Сочетание': m[7],
        'Покрытие': coverMap[m[8]] || '—',
        'Теплост.': m[9] === '1' ? '100°C' : m[9] === '2' ? '200°C' : '—'
      };
    }
  };

  // ───── ЕТ-СНЦ23 ─────
  // ЕТ-СНЦ23-3/14В-2-б-В
  const etSnc23 = {
    columns: ['Контакты', 'Размер', 'Тип', 'Патрубок', 'Поляриз.', 'Клим.'],
    parse(name) {
      const m = name.match(/^ЕТ-[СC]НЦ\s*23[\s-]*(\d+)\s*\/\s*(\d+)\s*([ВР])\s*-?\s*(\d+)\s*-?\s*([а-гА-Г])?\s*-?\s*([ВБ])?/);
      if (!m) return null;
      return {
        'Контакты': m[1],
        'Размер': m[2],
        'Тип': m[3] === 'В' ? 'Вилка' : 'Розетка',
        'Патрубок': m[4],
        'Поляриз.': (m[5] || '—').toLowerCase(),
        'Клим.': m[6] || 'В'
      };
    }
  };

  // ───── ЕТ-СНЦ144 / ЕТ-144 ─────
  // ЕТ-СНЦ144 3/9ВО1 1-N F П C
  // ЕТ-144-3/9ВО11NFK
  const etSnc144 = {
    columns: ['Контакты', 'Размер', 'Тип', 'Монтаж', 'Покр. конт.', 'Поляриз.', 'Покр. корп.', 'Корпус', 'Хвостовик'],
    parse(name) {
      // ЕТ-144-КП* — защитные крышки, не разъёмы
      if (/^ЕТ-144-КП/.test(name)) return null;
      // Normalize: remove prefix
      const n = name.replace(/^ЕТ-(?:СНЦ\s*)?144[\s-]*/, '').replace(/^ЕТ-144[\s-]*/, '');
      const m = n.match(/(\d+)\s*\/\s*(\d+)\s*([ВРвр])\s*([ОП][12])?\s*(\d)?\s*-?\s*([NABCDE])?\s*([FWKT][AT]?)?\s*([ПК])?\s*([CL])?/i);
      if (!m) return null;
      const mountMap = { 'О1': 'На кабель', 'О2': 'На кабель (2)', 'П1': 'На панель', 'П2': 'На панель (2)' };
      const coatMap = { 'F': 'Кадмий', 'W': 'Хим. никель', 'K': 'Хим. пассив.', 'FT': 'Кадмий термо', 'TA': 'Хим. ник. термо' };
      return {
        'Контакты': m[1],
        'Размер': m[2],
        'Тип': m[3] === 'В' || m[3] === 'в' ? 'Вилка' : 'Розетка',
        'Монтаж': mountMap[m[4]] || m[4] || '—',
        'Покр. конт.': m[5] === '1' ? 'Золото' : m[5] === '2' ? 'Серебро' : '—',
        'Поляриз.': m[6] || 'N',
        'Покр. корп.': coatMap[m[7]] || m[7] || '—',
        'Корпус': m[8] === 'П' ? 'Прямой' : m[8] === 'К' ? 'Угловой' : '—',
        'Хвостовик': m[9] === 'C' ? 'Обжим' : m[9] === 'L' ? 'Пайка' : '—'
      };
    }
  };

  // ───── ЕТ-ОНЦ-БМ / ЕТ-ОНЦ-БС ─────
  // ЕТ-ОНЦ-БМ-1 4 10 В 1 1 В
  const etOncBm = {
    columns: ['Покрытие', 'Контакты', 'Размер', 'Тип', 'Конструктив', 'Поляриз.', 'Клим.'],
    parse(name) {
      // ЕТ-ОНЦ-БМ-1-10/10-В1-1-В и ЕТ-ОНЦ-БС-1-10/14-В1-2-В
      const m = name.match(/^ЕТ-ОНЦ-Б[МС][\s-]*(\d)[\s-]*(\d+)\s*\/\s*(\d+)[\s-]*([ВР])(\d+)[\s-]*(\d+)[\s-]*([ВБ])?/);
      if (!m) return null;
      return {
        'Покрытие': m[1] === '1' ? 'Серебро' : m[1] === '2' ? 'Золото' : m[1],
        'Контакты': m[2],
        'Размер': m[3],
        'Тип': m[4] === 'В' ? 'Вилка' : 'Розетка',
        'Конструктив': m[5],
        'Поляриз.': m[6],
        'Клим.': m[7] || 'В'
      };
    }
  };

  // ───── ЕТ-РРС (3/4/5/6) ─────
  // ЕТ-РРС3-4А-1-1-у-В
  const etRrs = {
    columns: ['Серия', 'Контакты', 'Покрытие', 'Конструктив', 'Поляриз.', 'Фланец', 'Клим.'],
    parse(name) {
      // «З» (кирилл.) = «3» (цифра) в прайсе
      const norm = name.replace('РРСЗ', 'РРС3');
      const m = norm.match(/^ЕТ-РРС\s*(\d)[пП]?[\s-]*(\d+)\s*([АA])?\s*-?\s*(\d+)\s*-?\s*(\d+)\s*-?\s*(у)?\s*-?\s*([ВБ])?/);
      if (!m) return null;
      return {
        'Серия': 'РРС' + m[1],
        'Контакты': m[2],
        'Покрытие': m[3] ? 'Золото' : 'Никель',
        'Конструктив': m[4],
        'Поляриз.': m[5],
        'Фланец': m[6] ? 'Уменьшенный' : 'Стандартный',
        'Клим.': m[7] || 'В'
      };
    }
  };

  // ───── Базовый парсер (для серий без PDF) ─────
  const basic = {
    columns: [],
    parse() { return null; }
  };

  // Маппинг slug → парсер
  const MAP = {
    'et-2rmt':   et2rmt,
    'et-2rtt':   et2rtt,
    'et-2rmg':   et2rmg,
    'et-snc23':  etSnc23,
    'et-snc144': etSnc144,
    'et-onc-bm': etOncBm,
    'et-onc-bs': etOncBm,
    'et-rrs':    etRrs,
  };

  /**
   * Получить парсер для серии
   * @param {string} slug
   * @returns {{ columns: string[], parse: (name: string) => object|null }}
   */
  function getParser(slug) {
    return MAP[slug] || basic;
  }

  return { getParser };
})();
